export class Card {
    constructor(color, card) {
        this.color = color;
        this.card = card;
    }

    getColor(){
        return this.color;
    }

    
    setColor(color){
        this.color = color;
    }

    getCard(){
        return this.card;
    }

    setCard(card){
        this.card = card;
    }


}

